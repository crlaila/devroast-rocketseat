async function seed() {
  const pool = new Pool({ connectionString: process.env.DATABASE_URL });
  const db = drizzle(pool, { casing: "snake_case" });

  console.log("🌱 Seeding database...\n");

  // clear existing data (order matters due to FKs)
  console.log("  Clearing existing data...");
  await db.delete(roastDiffs);
  await db.delete(roastIssues);
  await db.delete(submissions);

  const TOTAL = 50;

  for (let i = 0; i < TOTAL; i++) {
    const lang = faker.helpers.arrayElement(LANGUAGES);
    const code = pickCodeSnippet(lang);
    const lineCount = code.split("\n").length;
    const scoreStr = pickScore();
    const score = parseFloat(scoreStr);
    const verdict = pickVerdict(score);
    const shameScore = (
      (10 - score) *
      faker.number.float({ min: 0.8, max: 1.2, fractionDigits: 2 })
    ).toFixed(2);
    const roastMode = faker.datatype.boolean({ probability: 0.4 });
    const isPublic = faker.datatype.boolean({ probability: 0.9 });
    const roastQuote = faker.helpers.arrayElement(ROAST_QUOTES);
    const createdAt = faker.date.between({
      from: new Date("2025-01-01"),
      to: new Date(),
    });

    // insert submission
    const [sub] = await db
      .insert(submissions)
      .values({
        code,
        language: lang,
        lineCount,
        roastMode,
        score: scoreStr,
        verdict,
        roastQuote,
        isPublic,
        shameScore,
        createdAt,
      })
      .returning({ id: submissions.id });

    // insert issues (2–4 per submission)
    const issueCount = faker.number.int({ min: 2, max: 4 });
    const picked = pickIssues(issueCount);
    await db.insert(roastIssues).values(
      picked.map((issue, idx) => ({
        submissionId: sub.id,
        severity: issue.severity,
        badgeLabel: issue.badgeLabel,
        title: issue.title,
        description: issue.description,
        sortOrder: idx,
      })),
    );

    // insert diff
    const diff = faker.helpers.arrayElement(DIFF_TEMPLATES);
    await db.insert(roastDiffs).values(
      diff.lines.map((line, idx) => ({
        submissionId: sub.id,
        lineType: line.lineType,
        content: line.content,
        lineNumber: idx + 1,
        sortOrder: idx,
        fileName: diff.fileName,
      })),
    );

    const progress = `[${String(i + 1).padStart(2, " ")}/${TOTAL}]`;
    console.log(
      `  ${progress} ${lang.padEnd(12)} score: ${scoreStr.padStart(4)}  verdict: ${verdict}`,
    );
  }

  await pool.end();

  console.log("\n✅ Seed complete.");
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});

function pickVerdict(score: number): Verdict {
  if (score <= 2.0) return "legendary_garbage";
  if (score <= 4.0) return "needs_serious_help";
  if (score <= 6.0) return "could_be_worse";
  if (score <= 8.0) return "not_terrible";
  return "almost_decent";
}

function pickScore(): string {
  // weighted toward low scores (this IS a shame leaderboard)
  const weights = [
    { max: 2.0, weight: 30 },
    { max: 4.0, weight: 35 },
    { max: 6.0, weight: 20 },
    { max: 8.0, weight: 10 },
    { max: 10.0, weight: 5 },
  ];
  const total = weights.reduce((s, w) => s + w.weight, 0);
  let rand = faker.number.int({ min: 1, max: total });
  let min = 0;
  for (const { max, weight } of weights) {
    rand -= weight;
    if (rand <= 0) {
      const val = faker.number.float({ min, max, fractionDigits: 1 });
      return val.toFixed(1);
    }
    min = max;
  }
  return "5.0";
}

function pickIssues(count: number) {
  const shuffled = faker.helpers.shuffle([...ISSUE_POOL]);
  return shuffled.slice(0, count);
}

function pickCodeSnippet(lang: string): string {
  const snippets = CODE_SNIPPETS[lang] ?? CODE_SNIPPETS.javascript;
  return faker.helpers.arrayElement(snippets);
}

